//Las variables son contenedores que almacenan datos. En JavaScript, puedes declarar variables usando `let`, `const` o `var`.
// `let` se utiliza para declarar variables que pueden cambiar su valor, `const` para declarar constantes que no cambian su valor, y `var` es una forma más antigua de declarar variables que tiene un alcance diferente.
// Declaración de variable y valor asignado
let nombre = "Emir";
let apellido = "Polito";
let edad = 18;

// Imprimir las variables en la consola
console.log(nombre); // Imprime: Emir
console.log(apellido); // Imprime: Polito
console.log(edad); // Imprime: 18