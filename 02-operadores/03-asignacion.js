// En este caso, se utilizan para modificar el valor de una variable existente.

let a = 10;

console.log(a > 5);// Compara si a es mayor que 5
console.log(a < 5);// Compara si a es menor que 5
console.log(a >= 5);// Compara si a es mayor o igual que 5
console.log(a <= 5);// Compara si a es menor o igual que 5
console.log(a == 5);// Compara si a es igual a 5

console.log(a != 5);// Devuelve true si 'a' es diferente de 5 (solo compara el valor)
console.log(a === 5);// Devuelve true si 'a' es exactamente igual a 5 (valor y tipo)
console.log(a !== 5);// Devuelve true si 'a' es diferente de 5 o de distinto tipo (valor y tipo)
